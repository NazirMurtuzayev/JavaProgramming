package day03_EscapeSequences;
/*
Escape sequences: Must be given within double quote
  \n: start a new line
  \t: tab paragraph space
  \\: one backlslash
  \": double quote

 */
public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Java \nPython \nC#");

        System.out.println("----------------------------");

        System.out.println("Hello Cydeo \nHow are you all today? \nIt's nice see you all! \nWhat class do we have next week? ");

        System.out.println("------------------------------------");

        System.out.println("\tJava is cool Programming Language");

        System.out.println("-------------------------------------");

        System.out.println("/ \\"); // in order to print one backward slash you need two backlash

        System.out.println("----------------------------------------");

        System.out.println(" My favorite TV show is \"Game of Thrones\""); // in order to use double quote we have to use \" escape sequence




    }





}
