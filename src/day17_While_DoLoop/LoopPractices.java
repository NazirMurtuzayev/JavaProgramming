package day17_While_DoLoop;

public class LoopPractices {
    public static void main(String[] args) {


        for (int i = 0; i <=10; i++) {
            System.out.println(i);
        }
        System.out.println("---------------------");
        int j=1;
        while(j<=10){
            j++;
            System.out.println(j);
        }

        System.out.println("----------");
        int k=1;
        do{

            System.out.println(k);
        }while(k<=10);




    }
}
